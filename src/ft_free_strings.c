#include "push_swap.h"

void	**ft_free_string(char **str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		free(str[i]);
		i++;
	}
	write(1, "Error", 5);
	write(1, "\n", 1);
	free(str);
	exit (1);
}
